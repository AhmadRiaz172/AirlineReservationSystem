public class Passenger {
    
    private int passenger_id;
    private String passenger_name;
    private String passenger_gender;
    private int passenger_age;

    /**
     * @return int return the passenger_id
     */
    public int getPassenger_id() {
        return passenger_id;
    }

    /**
     * @param passenger_id the passenger_id to set
     */
    public void setPassenger_id(int passenger_id) {
        this.passenger_id = passenger_id;
    }

    /**
     * @return String return the passenger_name
     */
    public String getPassenger_name() {
        return passenger_name;
    }

    /**
     * @param passenger_name the passenger_name to set
     */
    public void setPassenger_name(String passenger_name) {
        this.passenger_name = passenger_name;
    }

    /**
     * @return String return the passenger_gender
     */
    public String getPassenger_gender() {
        return passenger_gender;
    }

    /**
     * @param passenger_gender the passenger_gender to set
     */
    public void setPassenger_gender(String passenger_gender) {
        this.passenger_gender = passenger_gender;
    }

    /**
     * @return int return the passenger_age
     */
    public int getPassenger_age() {
        return passenger_age;
    }

    /**
     * @param passenger_age the passenger_age to set
     */
    public void setPassenger_age(int passenger_age) {
        this.passenger_age = passenger_age;
    }

}
